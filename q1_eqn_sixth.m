function eqn1 = q1_eqn_sixth(q1val, q2val, q3val, r, t, eps, d, e, f, q_BC, k)

eqn1 = eps * eps * (([zeros(1, 1 / k); q1val(1:(1 / k) - 3, :)] ...
    + [q1val(2:(1 / k) - 2, :); q_BC(1, :)] ...
    - 2.0 * q1val) / (k^2) ... % Second r deriv
    + r.^(-1) .* ([q1val(:, 2:(1 / k)) zeros((1 / k) - 2, 1)] ...
    + [zeros((1 / k) - 2, 1) q1val(:, 1:(1 / k) - 1)] ...
    - 2.0 * q1val) / (k^2) ... % Second theta deriv
    + r.^(-2) .* ([q1val(2:(1 / k) - 2, :); q_BC(1, :)] ...
    - [zeros(1, 1 / k); q1val(1:(1 / k) - 3, :)]) / (2.0 * k)) ... % First r deriv
    - (t * q1val - 3.0 * q1val.^2 + 3.0 * q2val.^2 ...
    - 3.0 * q3val.^2 / 2.0 + 2.0 * q1val.^3 + 2.0 * q1val .* q2val.^2 ...
    + 2.0 * q1val .* q3val.^2 ...
    + (d / 5.0) * (5.0 * sqrt(6.0) * q1val.^4 / 6.0 ...
    - sqrt(6.0) * q1val.^2 .* q2val.^2 ...
    + 5.0 * sqrt(6.0) * q1val.^2 .* q3val.^2 / 4.0 ...
    - sqrt(6.0) * q2val.^4 / 2.0 ...
    - sqrt(6.0) * q2val.^2 .* q3val.^2 / 4.0 ...
    + sqrt(6.0) * q3val.^4 / 4.0 ...
    + 3.0 * sqrt(2.0) * q1val .* q2val .* q3val.^2 / 2.0) ...
    + e * (q1val.^5 + 2.0 * q1val.^3 .* q2val.^2 ...
    + 2.0 * q1val.^3 .* q3val.^2 + q1val .* q2val.^4 ...
    + q1val .* q3val.^4 + 2.0 * q1val .* q2val.^2 .* q3val.^2) ...
    + ((f - e) / 6.0) ...
    * (q1val.^5 - 4.0 * q1val.^3 .* q2val.^2 ...
    + 2.0 * q1val.^3 .* q3val.^2 ...
    + 3.0 * sqrt(3.0) * q1val.^2 .* q2val .* q3val.^2 / 2.0 ...
    + 3.0 * q1val .* q2val.^4 ...
    - 3.0 * q1val .* q2val.^2 .* q3val.^2 ...
    - 3.0 * sqrt(3.0) * q2val.^3 .* q3val.^2 / 2.0 ...
    + 3.0 * q1val .* q3val.^4 / 4.0 ...
    + 3.0 * sqrt(3.0) * q2val .* q3val.^4 / 4.0));

end